package com.daemon.sistema.rpg.velho.modelo;

public class Formas {

	private int entender;
	private int criar;
	private int controlar;
	
	public Formas(int entender, int criar, int controlar) {
		this.entender = entender;
		this.criar = criar;
		this.controlar = controlar;
	}

	public int getEntender() {
		return entender;
	}

	public void setEntender(int entender) {
		this.entender = entender;
	}

	public int getCriar() {
		return criar;
	}

	public void setCriar(int criar) {
		this.criar = criar;
	}

	public int getControlar() {
		return controlar;
	}

	public void setControlar(int controlar) {
		this.controlar = controlar;
	}
	
	
}
