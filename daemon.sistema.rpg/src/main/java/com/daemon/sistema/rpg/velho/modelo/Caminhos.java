package com.daemon.sistema.rpg.velho.modelo;

public class Caminhos {

	private int fogo;
	private int agua;
	private int terra;
	private int ar;
	private int luz;
	private int trevas;
	private int plantas;
	private int animais;
	private int humanos;
	private int spiritum;
	private int arkanun;
	private int metamagia;
	
	public Caminhos(int fogo, int agua, int terra, int ar, int luz, int trevas, int plantas, int animais, int humanos,
			int spiritum, int arkanun, int metamagia) {
		this.fogo = fogo;
		this.agua = agua;
		this.terra = terra;
		this.ar = ar;
		this.luz = luz;
		this.trevas = trevas;
		this.plantas = plantas;
		this.animais = animais;
		this.humanos = humanos;
		this.spiritum = spiritum;
		this.arkanun = arkanun;
		this.metamagia = metamagia;
	}

	public int getFogo() {
		return fogo;
	}

	public void setFogo(int fogo) {
		this.fogo = fogo;
	}

	public int getAgua() {
		return agua;
	}

	public void setAgua(int agua) {
		this.agua = agua;
	}

	public int getTerra() {
		return terra;
	}

	public void setTerra(int terra) {
		this.terra = terra;
	}

	public int getAr() {
		return ar;
	}

	public void setAr(int ar) {
		this.ar = ar;
	}

	public int getLuz() {
		return luz;
	}

	public void setLuz(int luz) {
		this.luz = luz;
	}

	public int getTrevas() {
		return trevas;
	}

	public void setTrevas(int trevas) {
		this.trevas = trevas;
	}

	public int getPlantas() {
		return plantas;
	}

	public void setPlantas(int plantas) {
		this.plantas = plantas;
	}

	public int getAnimais() {
		return animais;
	}

	public void setAnimais(int animais) {
		this.animais = animais;
	}

	public int getHumanos() {
		return humanos;
	}

	public void setHumanos(int humanos) {
		this.humanos = humanos;
	}

	public int getSpiritum() {
		return spiritum;
	}

	public void setSpiritum(int spiritum) {
		this.spiritum = spiritum;
	}

	public int getArkanun() {
		return arkanun;
	}

	public void setArkanun(int arkanun) {
		this.arkanun = arkanun;
	}

	public int getMetamagia() {
		return metamagia;
	}

	public void setMetamagia(int metamagia) {
		this.metamagia = metamagia;
	}

	
	
}
